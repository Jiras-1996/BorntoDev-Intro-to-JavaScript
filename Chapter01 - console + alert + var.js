// Chapter 01 : ตัวแปร console , alert และ var

// 1. console.log => ใช้แสดงข้อความในรูปแบบ log
console.log("Hello World")
console.log("Hello Jay")

// 2. alert => ใช้แสดงข้อความแบบกล่องข้อความแจ้งเตือน
alert("Hello Jay")
alert(10)

// 3. var => ใช้ในการประกาศตัวแปร และ ตัวแปรในjsเป็นแบบ case-sensitive
var name = "Jay"
console.log(name)
console.log(name)

// 4. การ Update ค่าในตัวแปร
name = "Pop"
console.log(name)
name = "James"
console.log(name)

// Challenge : ประกาศตัวแปร firstname กับ lastname
var firstname = "jiras"
var lastname = "adthsutanan"
console.log(firstname)
console.log(lastname)
